package model.enums;

/**
 * Enum defines the various room types available in the game.
 *
 * @author Arafa Mohamed
 * @version 10/26/24
 */
public enum RoomType {

    /** Represents a regular room with no special attributes. */
    Standard,

    /** Indicates the starting room where the game begins. */
    Start,

    /** Represents the exit room, where the game ends or victory is achieved. */
    Exit

}




